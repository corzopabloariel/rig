    {
        $this->data = $data;
    }

    /**
     * Build the message.
     *
     * @return $this
     */
    public function build()
    {
        $mail = $this
            ->subject('NotificaciÃ³n del sistema - RIG')->view('email.notice')
            ->with($this->data);
        return $mail;
    }
}
